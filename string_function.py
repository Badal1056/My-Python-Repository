str = "Hello! My name is Badal."
print(str)#To print string
print(len(str))#To print length of the string
print(type(str))#To print type of the string
print(str.lower())#To print string in lowercase
print(str.upper())#To print string in uppercase
print(str.strip())#To remove spaces at the beginning and at the end of the string
print(str.isnumeric())#To check all characters in string is numeric then true else false
print(str.isalpha())#To check all characters in string is alphabetic then true else false
print(str.isalnum())#To check all characters in string is alphanumeric then true else false
print(str.replace("Hello","Hi"))#To replace one string character to another string character
print(str.find("My"))#To find the particular character
print(str.index("name"))#To find index number
